def search(request):
    result_list=[]
    if request.method == 'POST':
        query = request.POST['query'].strip()
        if query:
            result_list = run_query(query)
        else: 
            query = ''
    return render(request, 'rango/search.html', {'result_list': result_list, 'query': query})

